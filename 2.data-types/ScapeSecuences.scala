/**
 * <----- Secuencias de escape ----->
 * Estas secuencias de escape tiene multiples usos como por ejemplo:
 *     -> saltos de linea
 *     -> nueva linea, etc
 *
 * @author jhondev-code
 */
object ScapeSecuences {
    def main(args: Array[String]): Unit = {
        val backspace: Character = '\b'
        val horizontalTab: Character = '\t'
        val formfeed: Character = '\f'
        val newline: Character = '\n'
        val carriageReturn: Character = '\r'
        val doubleQuote: Character = '\"'
        val singleQuote: Character = '\''
        val backslash: Character = '\\'
    }
}
